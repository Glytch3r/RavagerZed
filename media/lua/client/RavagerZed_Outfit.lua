require "lua_timers"

RavagerZed = RavagerZed or {}

function RavagerZed.isRavagerZed(zed)
    if not (zed or instanceof(zed, "IsoZombie")) then return false end
    return zed:getOutfitName() == "RavagerZed"
end

function RavagerZed.isRavagerChar(targ)
    local isRavagerZed = (instanceof(targ, "IsoZombie") and RavagerZed.isRavagerZed(targ))
    local isRavagerPl = (instanceof(targ, "IsoPlayer") and RavagerZed.isRavagerPl(targ))
    return isRavagerZed or isRavagerPl
end


function RavagerZed.isRavagerPl(pl)
	pl = pl or getPlayer()
	if not instanceof(pl, "IsoPlayer") then return false end
	--if not pl:isAccessLevel('admin') then return false end
	local wornItems = pl:getWornItems()
	if wornItems then
		for i=1, wornItems:size() do
			local item = wornItems:get(i-1):getItem()
			if item:getFullType() == "Base.RavagerZed" then
				return true
			end
		end
	end
	return false
end
function RavagerZed.setOutfit(targ)
    if targ then
        targ:dressInNamedOutfit("RavagerZed")
        targ:resetModelNextFrame()
    end
end