RavagerZed = RavagerZed or {}

function RavagerZed.cleanUp(zed)
    if zed then
        local vis = zed:getHumanVisual()
        local iVis = zed:getItemVisuals()
        local bVis = vis:getBodyVisuals()
        local attachedItems = zed:getAttachedItems()
        if vis and iVis and bVis and attachedItems then
            zed:clearAttachedItems()
            vis:randomDirt()
            vis:removeBlood()

            for i=1, BloodBodyPartType.MAX:index() do
                local part = BloodBodyPartType.FromIndex(i-1)
                vis:setBlood(part, 0)
                vis:setDirt(part, 0)
            end

            for i = 0, iVis:size() - 1 do
                local item = iVis:get(i)
                if item then
                    for j = 0, BloodBodyPartType.MAX:index() - 1 do
                        local part = BloodBodyPartType.FromIndex(j)
                        if item:getHole(part) ~= 0 then
                            item:removeHole(j)
                        end
                        item:setBlood(part, 0)
                        item:setDirt(part, 0)
                    end
                    if item:getInventoryItem() then
                        item:setInventoryItem(nil)
                    end
                end
            end

            for j = bVis:size() - 1, 0, -1 do
                local item = bVis:get(j)
                if item then
                    local fType = item:getItemType()
                    if luautils.stringStarts(fType, "Base.Hat_") or luautils.stringStarts(fType, "Base.ZedDmg_") or luautils.stringStarts(fType, "Base.Wound_") or luautils.stringStarts(fType, "Base.Bandage_") or luautils.stringStarts(fType, "Base.MakeUp_") or luautils.stringStarts(fType, "Base.RavagerZed")  then
                        vis:removeBodyVisualFromItemType(fType)
                    end
                end
            end
            vis:setHairModel('Bald')

            zed:resetModelNextFrame()
            zed:resetModel()

        end
    end
end
