function RavagerZed.TempMarker(sq, isGood)
	if not sq then return end
	local r, g, b = 1,0.4,0.4
	if isGood then
		r, g, b = 0.4,0.4,1
	end
	local mark = getWorldMarkers():addGridSquareMarker("circle_center", "circle_only_highlight", sq,r,g,b, true, 0.5)
	TestMarkers.add(mark);
	timer:Simple(sec, function()
		mark:remove()
	end)
end



function RavagerZed.setStats(zed)
    if zed then
        if zed:isReanimatedPlayer() then
            return
        end
        if RavagerZed.isRavagerZed(zed) and zed:getModData()['RavagerZed_Init'] == nil then
            local sandOpt = getSandboxOptions()

            sandOpt:set("ZombieLore.Speed", 2) 		-- 1 sprinters  		2 fast shamblers  	3 shamblers 		4 random
            sandOpt:set("ZombieLore.Strength",1)  	-- 1 superhuman 		2 normal 			3 weak 				4 random
            sandOpt:set("ZombieLore.Toughness",1)	-- 1 tough 				2 normal 			3 fragile 			4 random
            sandOpt:set("ZombieLore.Cognition",1) 	-- 1 navigate + doors 	2 navigate 			3 basic navigation 	4 random
            sandOpt:set("ZombieLore.Memory",1) 		 --1 long 				2 normal 			3 short				4 none 			5 random
            sandOpt:set("ZombieLore.Sight",1) 		-- 1 eagle 				2 normal 			3 poor 				4 random
            sandOpt:set("ZombieLore.Hearing",1) 	-- 1 pinpoint 			2 normal 			3 poor 				4 random



            --RavagerZed.setTurnSpeed(zed, 2)
            zed:setVariable('isRavagerZed', 'true')
            zed:makeInactive(true);
            zed:makeInactive(false);
  
            --zed:dressInPersistentOutfit("RavagerZed")
            RavagerZed.cleanUp(zed)
            zed:setTurnDelta(12)
            zed:getModData()['RavagerZed_Init'] = true
        end
    end
end

function RavagerZed.isUnarmed(pl, wpn)
	return (tostring(WeaponType.getWeaponType(pl)) == 'barehand' or (wpn and wpn:getCategories():contains("Unarmed"))) or wpn == nil
end
function RavagerZed.isFirearm(pl, wpn)
	if not wpn then return false end
	if wpn:isAimedFirearm() then return true end
	if RavagerZed.isUnarmed(pl, wpn) then return false end
	return wpn:getScriptItem() and wpn:getScriptItem():isRanged()
end
