RavagerZed = RavagerZed or {}


function RavagerZed.handleCorpse(corpse)
    if RavagerZed.isDeadRavager(corpse) or corpse:getModData()['RavagerZed_Init']  then
        local w = corpse:getWornItems()
        for i = 0, w:size() - 1 do
            local item = w:getItemByIndex(i)
            if item then
                local fType = "Base.RavagerZed"
                if fType and item:getFullType() == fType then
                    local sq = corpse:getSquare()
                    if sq then
                        sq:AddWorldInventoryItem("Base.RavagerZed_Corpse", ZombRand(0, 1.5), ZombRand(0, 1.5), 0)
                        RavagerZed.SpawnRewards(sq)
                        sq:removeCorpse(corpse, isClient())
                    end
                end
            end
        end
    end
end

function RavagerZed.CorpseCleaner()
    local rad = 8
    local pl = getPlayer(); if not pl then return end
    local cell = pl:getCell()
    local x, y, z = pl:getX(), pl:getY(), pl:getZ()
    for xDelta = -rad, rad do
        for yDelta = -rad, rad do
            local sq = cell:getOrCreateGridSquare(x + xDelta, y + yDelta, z)
            if sq then
                local corpse = IsoObjectPicker.Instance:PickCorpse(sq:getX(), sq:getY()) or sq:getDeadBody()
                if corpse and instanceof(corpse, "IsoDeadBody") then
                    RavagerZed.handleCorpse(corpse)
                end
            end
        end
    end
end
Events.EveryOneMinute.Remove(RavagerZed.CorpseCleaner)
--Events.EveryOneMinute.Add(RavagerZed.CorpseCleaner)

function RavagerZed.isDeadRavager(corpse)
	if not corpse or not instanceof(corpse, "IsoDeadBody") then return false end
    if corpse:getModData()['RavagerZed_Init'] then return true end
    local fit = tostring(corpse:getOutfitName())
    if fit == "RavagerZed" then return true end
	return false
end

