function RavagerZed.getSFX()
	local list = RavagerZed.sfxZedList
	return list[ZombRand(1, #list)]
end

function RavagerZed.preSFX(zed)
	if not RavagerZed.isRavagerZed(zed) then return end
	if zed:getEmitter() then
		local audioName = nil
		local list = RavagerZed.sfxBanList
		for _, audioName in ipairs(list) do
			if zed:getEmitter():isPlaying(audioName) then
				zed:getEmitter():stopSoundByName(tostring(audioName))
				getWorld():getFreeEmitter():stopSoundByName(tostring(audioName))
				RavagerZed.playRandSfx(zed)
			end
		end
	end
end
Events.OnZombieUpdate.Remove(RavagerZed.preSFX)
Events.OnZombieUpdate.Add(RavagerZed.preSFX)


function RavagerZed.playRandSfx(targ)
	targ = targ or getPlayer()
	local sfxRate = SandboxVars.RavagerZed.SFXRate or 0.8
	local roll = RavagerZed.doRoll(sfxRate)
	if roll then
		local toPlay = RavagerZed.getSFX()
		if isClient() then
			getSoundManager():PlayWorldSound(toPlay, targ:getSquare(), 0, 5, 5, false);
		else
			getSoundManager():playUISound(toPlay)
		end
		--zed:getEmitter():playSound(toPlay)
	end
end




function RavagerZed.keypress(key)
	local pl = getPlayer()
	if not RavagerZed.isRavagerPl(pl) then return end
    if key == getCore():getKey("Shout") then
		local toPlay = RavagerZed.getSFX()
		if isClient() then
			getSoundManager():PlayWorldSound(toPlay, pl:getSquare(), 0, 5, 5, false);
		else
			getSoundManager():playUISound(toPlay)
		end
    end
	return key
end
Events.OnKeyPressed.Remove(RavagerZed.keypress)
Events.OnKeyPressed.Add(RavagerZed.keypress)


function RavagerZed.OnZombieDead(zed)
	if not RavagerZed.isRavagerZed(zed) then return end
	RavagerZed.playSFX(zed, "RavagerZed_Hurt")
end
Events.OnZombieDead.Remove(RavagerZed.OnZombieDead)
Events.OnZombieDead.Add(RavagerZed.OnZombieDead)
