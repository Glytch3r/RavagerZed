function RavagerZed.VolumeHandler(dist, range)
    if dist >= range then
        return 0
    elseif dist <= 0 then
        return 1
    else
        return 1 - (dist / range)
    end
end



function RavagerZed.isOnAlert(zed)
	if not RavagerZed.isRavagerZed(zed) then return end
	return zed:isCurrentState(ZombieTurnAlerted.instance())
end

function RavagerZed.playSFX(zed, sfx)
	sfx = sfx or  RavagerZed.getSFX()
	if not RavagerZed.isRavagerZed(zed) then return end
	local range = 50
	local pl = getPlayer(); if not pl then return end
	if isClient() then
		if RavagerZed.isClosestPl(pl, zed) or pl == zed then
			local sq = zed:getSquare()
			if sq then
				local vol = RavagerZed.VolumeHandler(RavagerZed.checkDist(pl, zed), range)
				getSoundManager():PlayWorldSound(tostring(sfx), sq, 0, 50, vol, false);
			end
		end
	else
		pl:playSoundLocal(sfx)
	end
end


