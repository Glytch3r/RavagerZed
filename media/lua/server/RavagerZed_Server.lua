if isClient() then return; end

local Commands = {};
Commands.RavagerZed = {};

Commands.RavagerZed.isRavagerZed = function(player, args)
    local playerId = player:getOnlineID();
    sendServerCommand('RavagerZed', 'isRavagerZed', {id = playerId, isRavagerZed = args.isRavagerZed,   zedID = args.zedID})
end

Commands.RavagerZed.hitreaction = function(player, args)
    local playerId = player:getOnlineID();
    local hitreaction = args.hitreaction
    if hitreaction then
        sendServerCommand('RavagerZed', 'hitreaction', {hitreaction = tostring(hitreaction), zedID = args.zedID})
    end
end

Commands.RavagerZed.KnockDown = function(player, args)
    local playerId = player:getOnlineID();
    sendServerCommand('RavagerZed', 'KnockDown', {id = playerId, zedID = args.zedID})
end

Commands.RavagerZed.isRavagerPl = function(player, args)
    local playerId = player:getOnlineID();
    sendServerCommand('RavagerZed', 'isRavagerPl', {id = playerId, isRavagerPl =  args.isRavagerPl})
end

Commands.RavagerZed.Injury = function(player, args)
    local playerId = player:getOnlineID();
    sendServerCommand('RavagerZed', 'Injury', {id = playerId, targID = args.targID})
end



Commands.RavagerZed.doDespawn = function(player, args)
    local zed = RavagerZed.findzedID(tonumber(args.zedID))
    if zed then
        zed:removeFromWorld();
        zed:removeFromSquare();
    end
end


Commands.RavagerZed.doSpawn = function(player, args)
    local x, y, z, count, fit, fChance, isDown = args.x,  args.y,  args.z, args.count, args.fit, args.fChance, args.isDown
    local zed = addZombiesInOutfit(round(x), round(y), round(z), 1, tostring(fit), tonumber(fChance), isDown, false, isDown, isDown, 1.0)
    if zed then
        if isDown then
            if not zed:isOnFloor() then
                zed:knockDown(true)
            end
        end
    end
end

Events.OnClientCommand.Add(function(module, command, player, args)
	if Commands[module] and Commands[module][command] then
	    Commands[module][command](player, args)
	end
end)

